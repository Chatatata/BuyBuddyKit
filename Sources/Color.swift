import Foundation
import UIKit

extension UIColor {
    
    class func buddyGreen() -> UIColor {
        return UIColor(red: 0x14/255, green: 0xDA/255, blue: 0x9E/255, alpha: 1.0)
    }
    
    class func buddyRed() -> UIColor {
        return UIColor(red: 0xF9/255, green: 0x6C/255, blue: 0x6C/255, alpha: 1.0)
    }
    
    class func buddyYellow() -> UIColor{
        return UIColor(red:0xFF/255,green:0xD4/255,blue:0x52/255,alpha:1.0)
    }
}
